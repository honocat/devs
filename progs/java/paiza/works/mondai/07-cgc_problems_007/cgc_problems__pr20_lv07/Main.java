public class Main {
    public static void main(String[] args) {
        int x = 2;
        String str = "";

        if (x == 1) {
            str = "first";
        } else if (x == 2) {
            str = "second";
        } else if (x == 3) {
            str = "third";
        } else if (x == 4) {
            str = "fourth";
        }

        System.out.println(str);
    }
}